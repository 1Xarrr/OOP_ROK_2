using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1.Construction
{
    class Construction
    {
        public Construction(double height, double width, int entrances,
            int humanCapacity, string buildMaterial)
        {
            Height = height;
            Width = Width;
            Entrances = entrances;
            HumanCapacity = humanCapacity;
            BuildMaterial = buildMaterial

        }
            
        public double Height { get; set; }
        public double Width { get; set; }
        public int Entrances { get; set; }
        public int HumanCapacity { get; set; }
        public string BuildMaterial { get; set; }

    }
}




1.Height typu zmiennoprzecinkowego
2. Width typu zmiennoprzecinkowego
3. Entrances typu liczba całkowita
4. HumanCapacity typu liczba całkowita
5. BuildMaterial typu tekstowego